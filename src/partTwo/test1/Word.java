package partTwo.test1;

public class Word {

    /*
    Создаем несколько знаков пунктуции: Пробел, точка и переход на новую строку.
     */
    public String word(String word) {
        return word;
    }

    public String space() {
        return " ";
    }

    public String point() {
        return ".";
    }

    public String newLine() {
        return "\n";
    }
}
